public class DemoOperator{
  public static void main(String[] args) {
      // % (Find Remainder)
      int remainder = 5%2;
      System.out.println(remainder); //1
      System.out.println(5%6); //5
      int a = 15;
      a = a +1;
      a++;
      ++a;
      a +=1;
      System.out.println(a);
      int b = 20;
      b = b-1;
      b--;
      --b;
      b -=1;
      System.out.println(b);
      // double > long > int
      //Naming Convension: Caml Case "hoursPerDay"
  }
}